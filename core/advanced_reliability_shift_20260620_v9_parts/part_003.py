    'def _random_cut_depth(sample: np.ndarray, point: np.ndarray, rng: np.random.Generator, depth: int = 0, max_depth: int = 18) -> int:\n',
    '    if len(sample) <= 1 or depth >= max_depth:\n',
    '        return depth\n',
    '    minimum = np.min(sample, axis=0)\n',
    '    maximum = np.max(sample, axis=0)\n',
    '    ranges = maximum - minimum\n',
    '    total = float(np.sum(ranges))\n',
    '    if total <= 1e-12:\n',
    '        return depth\n',
    '    dimension = int(rng.choice(sample.shape[1], p=ranges / total))\n',
    '    cut = float(rng.uniform(minimum[dimension], maximum[dimension]))\n',
    '    left = sample[:, dimension] <= cut\n',
    '    point_left = point[dimension] <= cut\n',
    '    child = sample[left] if point_left else sample[~left]\n',
    '    if len(child) == 0 or len(child) == len(sample):\n',
    '        return depth + 1\n',
    '    return _random_cut_depth(child, point, rng, depth + 1, max_depth)\n',
