    'def lead_lag_path(path: np.ndarray) -> np.ndarray:\n',
    '    values = np.asarray(path, dtype=float)\n',
    '    if values.ndim != 2 or len(values) == 0:\n',
    '        return np.empty((0, 0))\n',
    '    result = np.empty((2 * len(values) - 1, 2 * values.shape[1]), dtype=float)\n',
    '    result[0] = np.concatenate([values[0], values[0]])\n',
    '    cursor = 1\n',
    '    for index in range(1, len(values)):\n',
    '        result[cursor] = np.concatenate([values[index], values[index - 1]])\n',
    '        result[cursor + 1] = np.concatenate([values[index], values[index]])\n',
    '        cursor += 2\n',
    '    return result\n',
    '\n',
    '\n',
    'def _signature_compose(levels_a: Sequence[np.ndarray], levels_b: Sequence[np.ndarray], dimension: int, level: int) -> list[np.ndarray]:\n',
    '    result = [np.array([1.0])]\n',
    '    for order in range(1, level + 1):\n',
    '        total = np.zeros(dimension ** order, dtype=float)\n',
    '        for split in range(order + 1):\n',
    '            left = levels_a[split]\n',
    '            right = levels_b[order - split]\n',
    '            total += np.kron(left, right)\n',
    '        result.append(total)\n',
    '    return result\n',
    '\n',
