    '    point = standardized_point[0]\n',
    '    rng = np.random.default_rng(int(seed) % (2**32 - 1))\n',
    '    depths = []\n',
    '    for _ in range(max(8, trees)):\n',
    '        size = min(len(data), sample_size)\n',
    '        subset = data[rng.choice(len(data), size=size, replace=False)]\n',
    '        depths.append(_random_cut_depth(subset, point, rng))\n',
    '    mean_depth = float(np.mean(depths))\n',
    '    score = 2.0 ** (-mean_depth / max(_average_path_length(min(len(data), sample_size)), 1e-9))\n',
