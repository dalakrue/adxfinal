    'def _rolling_tail_forecasts(losses: pd.Series, alpha: float, window: int = 240) -> pd.DataFrame:\n',
    '    values = pd.to_numeric(losses, errors="coerce")\n',
    '    var = values.shift(1).rolling(window, min_periods=max(60, window // 2)).quantile(1.0 - alpha)\n',
    '    def tail_mean(arr: np.ndarray) -> float:\n',
    '        arr = arr[np.isfinite(arr)]\n',
    '        if len(arr) < 20:\n',
    '            return np.nan\n',
    '        threshold = np.quantile(arr, 1.0 - alpha)\n',
    '        tail = arr[arr >= threshold]\n',
    '        return float(np.mean(tail)) if len(tail) else np.nan\n',
    '    es = values.shift(1).rolling(window, min_periods=max(60, window // 2)).apply(tail_mean, raw=True)\n',
    '    es = pd.concat([es, var], axis=1).max(axis=1)\n',
    '    return pd.DataFrame({"var": var.clip(lower=1e-12), "es": es.clip(lower=1e-12)})\n',
