    'def _kupiec_coverage(hits: np.ndarray, expected_rate: float) -> dict[str, Any]:\n',
    '    hits = np.asarray(hits, dtype=int)\n',
    '    n = len(hits)\n',
    '    x = int(hits.sum())\n',
    '    if n == 0:\n',
    '        return {"status": "UNAVAILABLE", "sample_count": 0}\n',
    '    p = min(max(float(expected_rate), 1e-12), 1 - 1e-12)\n',
    '    phat = min(max(x / n, 1e-12), 1 - 1e-12)\n',
    '    ll_null = (n - x) * math.log(1 - p) + x * math.log(p)\n',
    '    ll_alt = (n - x) * math.log(1 - phat) + x * math.log(phat)\n',
    '    lr = max(0.0, -2.0 * (ll_null - ll_alt))\n',
    '    p_value, source = chi_square_sf(lr, 1)\n',
    '    return {\n',
    '        "status": "PASS" if p_value is not None and p_value >= 0.05 else "WARNING",\n',
    '        "sample_count": n,\n',
    '        "breach_count": x,\n',
    '        "expected_rate": p,\n',
