    'def fz0_positive_loss_score(loss: float, var: float, es: float, alpha: float) -> float:\n',
    '    """Corrected FZ0 score under a positive adverse-loss convention.\n',
    '\n',
    '    S(L,v,e) = 1{L>=v}(L-v)/(alpha*e) + v/e + log(e) - 1,\n',
    '    with 0 < alpha < 1 and e >= v > 0. Lower is better.\n',
    '    """\n',
    '    l, v, e, a = float(loss), float(var), float(es), float(alpha)\n',
    '    if not (0.0 < a < 1.0 and e >= v > 0.0 and all(math.isfinite(x) for x in (l, v, e))):\n',
    '        raise ValueError("FZ0 positive-loss domain requires 0<alpha<1 and ES>=VaR>0 with finite loss.")\n',
    '    return (float(l >= v) * (l - v) / (a * e)) + (v / e) + math.log(e) - 1.0\n',
