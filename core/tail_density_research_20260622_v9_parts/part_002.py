    'def berkowitz_test_from_pits(pits: Sequence[float]) -> dict[str, Any]:\n',
    '    epsilon = 1e-8\n',
    '    p = np.asarray([min(max(float(x), epsilon), 1.0 - epsilon) for x in pits if math.isfinite(float(x))], dtype=float)\n',
    '    z = np.asarray([norm_ppf(x, epsilon) for x in p], dtype=float)\n',
    '    n = len(z)\n',
    '    if n < 25:\n',
    '        return {"status": "INSUFFICIENT_SAMPLE", "sample_count": n, "small_sample_warning": True}\n',
    '    null_ll = float(np.sum(-0.5 * (math.log(2.0 * math.pi) + z * z)))\n',
    '    y, x = z[1:], z[:-1]\n',
    '    x_mean, y_mean = float(np.mean(x)), float(np.mean(y))\n',
    '    denominator = float(np.sum((x - x_mean) ** 2))\n',
    '    rho = float(np.sum((x - x_mean) * (y - y_mean)) / denominator) if denominator > 1e-12 else 0.0\n',
    '    rho = float(np.clip(rho, -0.995, 0.995))\n',
    '    mu = float((y_mean - rho * x_mean) / (1.0 - rho)) if abs(1.0 - rho) > 1e-8 else y_mean\n',
    '    residuals = y - mu - rho * (x - mu)\n',
    '    variance = float(max(np.mean(residuals ** 2), 1e-12))\n',
    '    alternative_ll = float(np.sum(-0.5 * (math.log(2.0 * math.pi * variance) + (residuals ** 2) / variance)))\n',
    '    lr = max(0.0, 2.0 * (alternative_ll - null_ll))\n',
    '    p_value, source = chi_square_sf(lr, 3)\n',
    '    if p_value is None:\n',
    '        status = "WARNING"\n',
    '    elif p_value < 0.01:\n',
    '        status = "FAIL"\n',
    '    elif p_value < 0.05:\n',
    '        status = "WARNING"\n',
    '    else:\n',
    '        status = "PASS"\n',
    '    return {\n',
    '        "status": status,\n',
    '        "null_log_likelihood": null_ll,\n',
    '        "alternative_log_likelihood": alternative_ll,\n',
    '        "lr_statistic": lr,\n',
    '        "degrees_of_freedom": 3,\n',
    '        "approximate_p_value": p_value,\n',
    '        "p_value_source": source,\n',
    '        "estimated_mean": mu,\n',
    '        "estimated_variance": variance,\n',
    '        "estimated_rho": rho,\n',
    '        "sample_count": n,\n',
    '        "small_sample_warning": n < 100,\n',
    '        "pit_clip_epsilon": epsilon,\n',
    '    }\n',
