    'def _piecewise_pit(levels: np.ndarray, values: np.ndarray, realized: float) -> float:\n',
    '    # Explicit linear tails between (value_0 - scale, 0) and first quantile,\n',
    '    # and last quantile to (value_last + scale, 1).\n',
    '    scale = max(float(np.std(values)), abs(float(values[-1] - values[0])), 1e-6)\n',
    '    x = np.r_[values[0] - 2.0 * scale, values, values[-1] + 2.0 * scale]\n',
    '    p = np.r_[0.0, levels, 1.0]\n',
    '    return float(np.interp(realized, x, p, left=0.0, right=1.0))\n',
