    'def _knockoff_threshold(w: np.ndarray, q: float) -> float | None:\n',
    '    candidates = sorted(float(v) for v in np.unique(np.abs(w[np.isfinite(w)])) if float(v) > 0)\n',
    '    for threshold in candidates:\n',
    '        discoveries = int(np.sum(w >= threshold))\n',
    '        if discoveries <= 0:\n',
    '            continue\n',
    '        estimate = (1 + int(np.sum(w <= -threshold))) / discoveries\n',
    '        if estimate <= q:\n',
    '            return threshold\n',
    '    return None\n',
