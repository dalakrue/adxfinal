    'def gaussian_model_x_knockoffs(x: np.ndarray, *, seed: int) -> tuple[np.ndarray, dict[str, Any]]:\n',
    '    """Construct deterministic second-order Gaussian knockoffs.\n',
    '\n',
    '    This is an approximate mechanism when the covariate law is estimated.  The\n',
    '    returned metadata explicitly prevents an unconditional finite-sample FDR\n',
    '    claim unless exchangeability assumptions are independently established.\n',
    '    """\n',
    '    values = np.asarray(x, dtype=float)\n',
    '    if values.ndim != 2 or values.shape[0] < 4 or values.shape[1] < 1:\n',
    '        raise ValueError("knockoffs require a non-empty 2D feature matrix")\n',
    '    mean = np.mean(values, axis=0)\n',
    '    scale = np.std(values, axis=0, ddof=1)\n',
    '    scale = np.where(scale > 1e-9, scale, 1.0)\n',
    '    z = (values - mean) / scale\n',
    '    covariance = np.cov(z, rowvar=False)\n',
    '    covariance = np.atleast_2d(covariance)\n',
    '    p = covariance.shape[0]\n',
    '    covariance = covariance + np.eye(p) * 1e-5\n',
    '    eig = np.linalg.eigvalsh(covariance)\n',
    '    lambda_min = max(float(np.min(eig)), 1e-6)\n',
    '    s_value = min(1.0, 1.8 * lambda_min)\n',
    '    s = np.eye(p) * s_value\n',
    '    inv = np.linalg.pinv(covariance, rcond=1e-10)\n',
    '    conditional_mean = z @ (np.eye(p) - inv @ s)\n',
    '    conditional_cov = 2.0 * s - s @ inv @ s\n',
    '    conditional_cov = (conditional_cov + conditional_cov.T) / 2.0\n',
    '    evals, evecs = np.linalg.eigh(conditional_cov)\n',
    '    clipped = np.clip(evals, 0.0, None)\n',
    '    root = evecs @ np.diag(np.sqrt(clipped)) @ evecs.T\n',
    '    rng = np.random.default_rng(int(seed))\n',
    '    knock = conditional_mean + rng.standard_normal(z.shape) @ root.T\n',
    '    diagnostics = {\n',
    '        "construction": "second-order Gaussian equi-correlated knockoffs",\n',
    '        "estimated_covariate_distribution": True,\n',
    '        "minimum_covariance_eigenvalue": lambda_min,\n',
    '        "s_value": s_value,\n',
    '        "conditional_covariance_min_eigenvalue": float(np.min(clipped)),\n',
    '        "exact_model_x_exchangeability_proven": False,\n',
    '    }\n',
    '    return knock, diagnostics\n',
    '\n',
    '\n',
    'def _ridge_coefficients(design: np.ndarray, target: np.ndarray, ridge: float = 1.0) -> np.ndarray:\n',
    '    x = np.asarray(design, dtype=float)\n',
    '    y = np.asarray(target, dtype=float)\n',
    '    y = y - np.mean(y)\n',
    '    gram = x.T @ x + np.eye(x.shape[1]) * float(ridge)\n',
    '    return np.linalg.pinv(gram, rcond=1e-10) @ x.T @ y\n',
