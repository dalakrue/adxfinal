    'def update_adaptive_conformal_alpha(\n',
    '    previous: Mapping[str, Any] | None,\n',
    '    *,\n',
    '    horizon_hours: int,\n',
    '    miss: bool,\n',
    '    target_coverage: float = 0.90,\n',
    '    learning_rate: float = 0.01,\n',
    '    alpha_min: float = 0.01,\n',
    '    alpha_max: float = 0.40,\n',
    '    confirmed_drift: bool = False,\n',
