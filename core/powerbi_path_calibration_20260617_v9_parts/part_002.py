    '    lr = min(.05, max(.0001, float(learning_rate)))\n',
    '    # A miss lowers alpha -> higher effective coverage -> wider future interval.\n',
    '    alpha_after = alpha_before + lr * ((0.0 if miss else 1.0) - float(target_coverage))\n',
