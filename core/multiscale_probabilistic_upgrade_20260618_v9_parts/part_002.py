    '            main2.loc[idx, "upper_band"] = p90[:n]\n',
    '            main2.loc[idx, "band_width"] = np.maximum(p50[:n] - p10[:n], p90[:n] - p50[:n])\n',
    '            updated["main"] = main2\n',
    '            summary = dict(updated.get("summary") or {})\n',
