    '                "pip_contribution": round(float(contribution) * 10000.0, 5),\n',
    '                "direction": "POSITIVE" if contribution > 0 else "NEGATIVE" if contribution < 0 else "FLAT",\n',
    '                "percentage_of_total_movement": round(float(contribution / total * 100.0), 4) if abs(total) > 1e-12 else 0.0,\n',
    '            })\n',
