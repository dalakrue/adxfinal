    '        "tp_level": round(tp_level, 8), "sl_level": round(sl_level, 8),\n',
    '        "quantile_ordering_valid": bool(np.all(p10 <= p25) and np.all(p25 <= p50) and np.all(p50 <= p75) and np.all(p75 <= p90)),\n',
    '    }\n',
    '    updated = bundle\n',
    '    if isinstance(bundle, Mapping):\n',
    '        updated = dict(bundle)\n',
    '        main = bundle.get("main")\n',
    '        if isinstance(main, pd.DataFrame) and not main.empty:\n',
