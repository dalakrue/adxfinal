 '\n',
 'def _single_column_identity(frame: Any, names: Sequence[str]) -> set[str]:\n',
 '    if not isinstance(frame, pd.DataFrame) or frame.empty:\n',
 '        return set()\n',
 '    normalized = {str(c).strip().lower(): c for c in frame.columns}\n',
 '    for name in names:\n',
 '        col = normalized.get(name.lower())\n',
 '        if col is not None:\n',
 '            return {str(v) for v in frame[col].dropna().astype(str).unique()}\n',
 '    return set()\n',
