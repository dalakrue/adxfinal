 'def frame_to_shared_broker_clock(\n',
 '    frame: pd.DataFrame,\n',
 '    state: Mapping[str, Any],\n',
 '    *,\n',
 '    canonical: Mapping[str, Any] | None = None,\n',
 '    include_myanmar: bool = True,\n',
 '    reject_future_incomplete: bool = True,\n',
 '    hide_raw_utc: bool = True,\n',
 ') -> pd.DataFrame:\n',
 '    """Create a newest-first display projection from canonical UTC event times."""\n',
 '    if not isinstance(frame, pd.DataFrame) or frame.empty:\n',
 '        return frame\n',
 '    work = frame.copy(deep=False)\n',
 '    time_col = _authoritative_time_column(work)\n',
 '    if time_col is None and isinstance(work.index, pd.DatetimeIndex):\n',
 '        index_name = work.index.name or "event_time_utc"\n',
 '        work = work.reset_index().rename(columns={index_name: "event_time_utc", "index": "event_time_utc"})\n',
 '        time_col = "event_time_utc"\n',
 '    if time_col is None:\n',
 '        return work\n',
 '    parsed = pd.to_datetime(work[time_col], errors="coerce", utc=True)\n',
 '    work = work.loc[parsed.notna()].copy()\n',
 '    parsed = parsed.loc[work.index]\n',
 '    if work.empty:\n',
 '        return work.reset_index(drop=True)\n',
 '\n',
 '    # The frame itself is authoritative evidence. Passing it into the provider\n',
 '    # prevents an old restored canonical object from filtering out newer completed\n',
 '    # rows that are already present in the table being rendered.\n',
 '    contract = shared_broker_time_provider(state, frame=work, canonical=canonical)\n',
 '    watermark = _as_utc(contract.get("latest_completed_h1_utc"))\n',
 '    if reject_future_incomplete and watermark is not None:\n',
 '        keep = parsed.le(watermark)\n',
 '        work = work.loc[keep].copy()\n',
 '        parsed = parsed.loc[keep]\n',
 '    if work.empty:\n',
 '        return work.reset_index(drop=True)\n',
 '\n',
 '    work["__event_time_utc"] = parsed\n',
 '    # Preserve caller order; history query layers own newest-first ordering.\n',
 '    parsed = work["__event_time_utc"]\n',
 '    position = list(work.columns).index(time_col) if time_col in work.columns else 0\n',
 '    if hide_raw_utc and time_col in work.columns:\n',
 '        work = work.drop(columns=[time_col])\n',
 '\n',
 '    if not contract.get("broker_clock_available"):\n',
 '        broker_col = "Broker Time"\n',
 '        work.insert(min(position, len(work.columns)), broker_col, BROKER_TIME_UNAVAILABLE)\n',
 '        if include_myanmar:\n',
 '            myanmar = parsed.dt.tz_convert(_fixed_timezone(390)).dt.strftime("%Y-%m-%d %H:%M:%S")\n',
 '            work.insert(min(position + 1, len(work.columns)), "Myanmar Time (UTC+6:30)", myanmar)\n',
 '        # Never corrupt usable history dates with a configuration warning. When\n',
 '        # broker offset evidence is temporarily unavailable after navigation,\n',
 '        # preserve canonical candle identity and rebuild visible calendar fields\n',
 '        # from the authoritative UTC event timestamp. The Broker Time column stays\n',
 '        # explicit about the missing offset; no broker time is guessed.\n',
 '        canonical_utc = parsed.dt.tz_convert("UTC")\n',
 '        work["Date"] = canonical_utc.dt.strftime("%Y-%m-%d")\n',
 '        work["Weekday"] = canonical_utc.dt.strftime("%A")\n',
 '        work["Hour"] = canonical_utc.dt.strftime("%H:00")\n',
 '        # Repeated display projections must not leave a second stale time alias.\n',
 '        for column in list(work.columns):\n',
 '            normalized = str(column).strip().lower().replace("_", " ")\n',
 '            if normalized in _DISPLAY_CLOCK_ALIASES and column != broker_col:\n',
 '                work[column] = canonical_utc.dt.tz_localize(None)\n',
 '    else:\n',
 '        resolution_state = _state_with_saved_broker_clock(state, canonical if isinstance(canonical, Mapping) else '
 '_canonical_from_state(state))\n',
 '        tzinfo = resolve_broker_clock(resolution_state, event_time_utc=watermark).get("broker_tzinfo")\n',
 '        broker = parsed.dt.tz_convert(tzinfo)\n',
 '        label = str(contract.get("broker_offset_label"))\n',
 '        broker_col = f"Broker Time (UTC{label})"\n',
 '        work.insert(min(position, len(work.columns)), broker_col, broker.dt.tz_localize(None))\n',
 '        if include_myanmar:\n',
 '            myanmar = parsed.dt.tz_convert(_fixed_timezone(390)).dt.tz_localize(None)\n',
 '            work.insert(min(position + 1, len(work.columns)), "Myanmar Time (UTC+6:30)", myanmar)\n',
 '        # Always rebuild these visible fields from broker time, even when they\n',
 '        # already existed with stale UTC-derived values.\n',
 '        work["Date"] = broker.dt.strftime("%Y-%m-%d")\n',
 '        work["Weekday"] = broker.dt.strftime("%A")\n',
 '        work["Hour"] = broker.dt.strftime("%H:00")\n',
 '        # Every visible candle-time alias is rebuilt from the same broker-aware\n',
 '        # timestamp. This prevents Date/Hour/Completed Candle disagreements.\n',
 '        for column in list(work.columns):\n',
 '            normalized = str(column).strip().lower().replace("_", " ")\n',
 '            if normalized in _DISPLAY_CLOCK_ALIASES and column != broker_col:\n',
 '                work[column] = broker.dt.tz_localize(None)\n',
 '    work = work.drop(columns=["__event_time_utc"], errors="ignore")\n',
 '    return work.reset_index(drop=True)\n',
 '\n',
 '\n',
 'def latest_history_utc(frame: Any) -> pd.Timestamp | None:\n',
 '    return _frame_latest_utc(frame)\n',
