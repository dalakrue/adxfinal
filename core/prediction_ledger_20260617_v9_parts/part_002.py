    '        idx = pd.to_datetime(out.index, utc=True, errors="coerce")\n',
    '    out = pd.DataFrame({name: pd.to_numeric(out[col], errors="coerce").to_numpy() for name, col in required.items()}, index=pd.DatetimeIndex(idx))\n',
