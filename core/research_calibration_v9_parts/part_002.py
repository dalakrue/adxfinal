    'def bayesian_online_changepoint(frame: pd.DataFrame, hazard_lambda: float = 72.0, max_run_length: int = 240) -> Dict[str, Any]:\n',
    '    market = normalize_completed_ohlc(frame).tail(720)\n',
    '    if len(market) < 36:\n',
    '        return {"status": "INSUFFICIENT SAMPLE", "sample_size": int(len(market)), "probability_change_now": 0.5}\n',
    '    values = _change_feature(market)\n',
    '    observation_variance = max(float(np.var(values[: max(24, len(values) // 3)])), 0.35)\n',
    '    hazard = 1.0 / max(float(hazard_lambda), 2.0)\n',
    '    run = np.array([1.0], dtype=float)\n',
    '    means = np.array([0.0], dtype=float)\n',
    '    counts = np.array([0.0], dtype=float)\n',
    '    cp_history: list[float] = []\n',
    '    for value in values:\n',
    '        predictive_variance = observation_variance * (1.0 + 1.0 / (counts + 1.0))\n',
    '        log_like = -0.5 * (np.log(2.0 * math.pi * predictive_variance) + np.square(value - means) / predictive_variance)\n',
    '        likelihood = np.exp(np.clip(log_like - np.max(log_like), -80.0, 0.0))\n',
    '        growth = run * likelihood * (1.0 - hazard)\n',
    '        change = float(np.sum(run * likelihood * hazard))\n',
    '        new_run = np.concatenate([[change], growth])[: max_run_length + 1]\n',
    '        total = float(new_run.sum())\n',
    '        new_run = new_run / total if total > 0 else np.array([1.0])\n',
    '        new_means = np.empty_like(new_run)\n',
    '        new_counts = np.empty_like(new_run)\n',
    '        new_means[0] = value; new_counts[0] = 1.0\n',
    '        keep = len(new_run) - 1\n',
    '        if keep:\n',
    '            prior_counts = counts[:keep]\n',
    '            prior_means = means[:keep]\n',
    '            new_counts[1:] = prior_counts + 1.0\n',
    '            new_means[1:] = (prior_means * prior_counts + value) / np.maximum(new_counts[1:], 1.0)\n',
    '        run, means, counts = new_run, new_means, new_counts\n',
    '        cp_history.append(float(run[0]))\n',
    '    run_lengths = np.arange(len(run), dtype=float)\n',
    '    expected = float(np.sum(run_lengths * run))\n',
    '    most_likely = int(np.argmax(run))\n',
    '    p_now = float(run[0])\n',
