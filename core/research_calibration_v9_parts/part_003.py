    'def deflated_sharpe_ratio(\n',
    '    strategy_returns: Optional[Sequence[float]], *, number_of_trials: Optional[int], sharpe_trials: Optional[Sequence[float]] = None,\n',
    ') -> Dict[str, Any]:\n',
    '    return_values = [] if strategy_returns is None else list(strategy_returns)\n',
    '    values = np.asarray(return_values, dtype=float)\n',
    '    values = values[np.isfinite(values)]\n',
    '    trials = int(number_of_trials or 0)\n',
    '    trial_sequence = [] if sharpe_trials is None else list(sharpe_trials)\n',
    '    trial_values = np.asarray(trial_sequence, dtype=float)\n',
    '    trial_values = trial_values[np.isfinite(trial_values)]\n',
    '    if len(values) < 30:\n',
    '        return {"raw_sharpe": None, "deflated_sharpe_statistic": None, "deflated_sharpe_probability": None, "number_of_trials": trials, "sample_size": int(len(values)), "skewness": None, "kurtosis": None, "validation_label": "UNAVAILABLE", "unavailable_reason": "Need at least 30 aligned actual strategy returns"}\n',
    '    if trials < 2 or len(trial_values) < 2:\n',
    '        return {"raw_sharpe": None, "deflated_sharpe_statistic": None, "deflated_sharpe_probability": None, "number_of_trials": trials, "sample_size": int(len(values)), "skewness": None, "kurtosis": None, "validation_label": "UNAVAILABLE", "unavailable_reason": "Need the number and Sharpe distribution of tested strategies/configurations"}\n',
    '    mean = float(np.mean(values)); std = float(np.std(values, ddof=1))\n',
    '    if std <= 0:\n',
    '        return {"raw_sharpe": None, "deflated_sharpe_statistic": None, "deflated_sharpe_probability": None, "number_of_trials": trials, "sample_size": int(len(values)), "skewness": None, "kurtosis": None, "validation_label": "UNAVAILABLE", "unavailable_reason": "Return variance is zero"}\n',
    '    centered = (values - mean) / std\n',
    '    skewness = float(np.mean(centered ** 3)); kurtosis = float(np.mean(centered ** 4))\n',
    '    raw_sharpe = mean / std * math.sqrt(252.0)\n',
    '    trial_std = max(float(np.std(trial_values, ddof=1)), 1e-12)\n',
    '    # Expected maximum Sharpe approximation under multiple testing.\n',
    '    expected_max = float(np.mean(trial_values) + trial_std * math.sqrt(2.0 * math.log(max(trials, 2))))\n',
    '    denominator = math.sqrt(max((1.0 - skewness * raw_sharpe + (kurtosis - 1.0) * raw_sharpe ** 2 / 4.0) / max(len(values) - 1, 1), 1e-12))\n',
    '    statistic = (raw_sharpe - expected_max) / denominator\n',
    '    probability = _normal_cdf(statistic)\n',
    '    return {\n',
    '        "raw_sharpe": round(raw_sharpe, 8), "deflated_sharpe_statistic": round(statistic, 8),\n',
    '        "deflated_sharpe_probability": round(probability, 8), "number_of_trials": trials,\n',
    '        "sample_size": int(len(values)), "skewness": round(skewness, 8), "kurtosis": round(kurtosis, 8),\n',
    '        "validation_label": "ACCEPT" if probability >= 0.95 else "WEAK" if probability >= 0.70 else "REJECT",\n',
    '        "unavailable_reason": None,\n',
    '    }\n',
