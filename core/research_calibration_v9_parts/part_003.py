    'def probability_backtest_overfitting(performance_matrix: Any, metric_used: str = "net_return") -> Dict[str, Any]:\n',
    '    matrix = np.asarray(performance_matrix, dtype=float)\n',
    '    if matrix.ndim != 2 or matrix.shape[0] < 4 or matrix.shape[1] < 4:\n',
    '        return {\n',
    '            "value": None, "status": "UNAVAILABLE", "unavailable_reason": "Need at least 4 chronological periods and 4 tested configurations",\n',
    '            "number_of_periods": int(matrix.shape[0]) if matrix.ndim == 2 else 0,\n',
    '            "number_of_configurations": int(matrix.shape[1]) if matrix.ndim == 2 else 0,\n',
    '            "split_count": 0, "metric_used": metric_used,\n',
    '        }\n',
    '    periods = matrix.shape[0]\n',
    '    split_size = periods // 2\n',
    '    split_count = 0; below_median = 0\n',
    '    # Deterministic symmetric chronological combinations, bounded for speed.\n',
    '    import itertools\n',
    '    combinations = list(itertools.combinations(range(periods), split_size))\n',
    '    if len(combinations) > 200:\n',
    '        positions = np.linspace(0, len(combinations) - 1, 200, dtype=int)\n',
    '        combinations = [combinations[i] for i in positions]\n',
    '    seen: set[tuple[int, ...]] = set()\n',
    '    for insample in combinations:\n',
    '        if insample in seen:\n',
    '            continue\n',
    '        outsample = tuple(i for i in range(periods) if i not in insample)\n',
    '        seen.add(insample); seen.add(outsample)\n',
    '        in_scores = np.nanmean(matrix[list(insample)], axis=0)\n',
    '        out_scores = np.nanmean(matrix[list(outsample)], axis=0)\n',
    '        if not np.isfinite(in_scores).any() or not np.isfinite(out_scores).any():\n',
    '            continue\n',
    '        winner = int(np.nanargmax(in_scores))\n',
    '        ranks = pd.Series(out_scores).rank(method="average", pct=True).to_numpy(dtype=float)\n',
    '        if ranks[winner] < 0.5:\n',
    '            below_median += 1\n',
    '        split_count += 1\n',
    '    if split_count == 0:\n',
    '        return {"value": None, "status": "UNAVAILABLE", "unavailable_reason": "No valid symmetric splits", "number_of_periods": periods, "number_of_configurations": matrix.shape[1], "split_count": 0, "metric_used": metric_used}\n',
    '    value = below_median / split_count\n',
    '    return {\n',
    '        "value": round(float(value), 8), "status": "VALID", "unavailable_reason": None,\n',
    '        "number_of_periods": periods, "number_of_configurations": matrix.shape[1], "split_count": split_count,\n',
    '        "metric_used": metric_used, "validation_label": "HIGH OVERFITTING RISK" if value > 0.50 else "WATCH" if value > 0.25 else "ACCEPTABLE",\n',
    '    }\n',
