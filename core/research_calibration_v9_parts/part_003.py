    'def _bounded_normalize(raw: Mapping[str, float], low: float = 0.03, high: float = 0.78) -> Dict[str, float]:\n',
    '    """Project positive scores onto a bounded probability simplex.\n',
    '\n',
    '    A clip-then-renormalize loop can violate the maximum again after the final\n',
    '    normalization.  The monotone bisection below solves\n',
    '    ``sum(clip(lambda * score_i, low, high)) == 1`` and therefore preserves all\n',
    '    bounds and the unit-sum invariant at the same time.\n',
    '    """\n',
    '    positive = {\n',
    '        str(k): max(float(v), 0.0)\n',
    '        for k, v in raw.items()\n',
    '        if math.isfinite(float(v)) and float(v) > 0\n',
    '    }\n',
    '    if not positive:\n',
    '        return {}\n',
    '    count = len(positive)\n',
    '    # Make the requested box feasible for small/large active model sets.\n',
    '    lower = min(max(float(low), 0.0), 1.0 / count)\n',
    '    upper = max(min(float(high), 1.0), 1.0 / count)\n',
    '    values = np.asarray(list(positive.values()), dtype=float)\n',
    '\n',
    '    def mass(multiplier: float) -> float:\n',
    '        return float(np.clip(multiplier * values, lower, upper).sum())\n',
    '\n',
    '    left, right = 0.0, 1.0\n',
    '    while mass(right) < 1.0:\n',
    '        right *= 2.0\n',
    '        if right > 1e18:  # Defensive fallback; feasibility is guaranteed above.\n',
    '            break\n',
    '    for _ in range(100):\n',
    '        middle = (left + right) / 2.0\n',
    '        if mass(middle) < 1.0:\n',
    '            left = middle\n',
    '        else:\n',
    '            right = middle\n',
    '    projected = np.clip(((left + right) / 2.0) * values, lower, upper)\n',
    '    # Floating-point correction is distributed only over coordinates with room.\n',
    '    difference = 1.0 - float(projected.sum())\n',
    '    for _ in range(count + 2):\n',
    '        if abs(difference) <= 1e-14:\n',
    '            break\n',
    '        if difference > 0:\n',
    '            eligible = np.where(projected < upper - 1e-14)[0]\n',
    '        else:\n',
    '            eligible = np.where(projected > lower + 1e-14)[0]\n',
    '        if len(eligible) == 0:\n',
    '            break\n',
    '        increment = difference / len(eligible)\n',
    '        projected[eligible] = np.clip(projected[eligible] + increment, lower, upper)\n',
    '        difference = 1.0 - float(projected.sum())\n',
    '    return {key: float(value) for key, value in zip(positive.keys(), projected)}\n',
