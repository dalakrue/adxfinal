    'def _baseline_skill(\n',
    '    residual_bundle: Mapping[str, Any], baseline_errors: Mapping[str, Mapping[int, np.ndarray]],\n',
    ') -> Dict[str, Any]:\n',
    '    scalar = residual_bundle.get("scalar_bank")\n',
