    '        if isinstance(scalar, pd.DataFrame) and not scalar.empty:\n',
    '            system_errors = pd.to_numeric(scalar.loc[scalar["horizon"] == h, "residual"], errors="coerce").dropna().to_numpy(dtype=float)\n',
    '        for baseline, per_h in baseline_errors.items():\n',
    '            base = np.asarray(per_h.get(h, []), dtype=float)\n',
    '            base = base[np.isfinite(base)]\n',
    '            if len(system_errors) < 8 or len(base) < 8:\n',
    '                rows.append({"baseline_name": baseline, "horizon": h, "system_error": None, "baseline_error": None, "skill_score": None, "direction_comparison": "UNAVAILABLE", "calibration_comparison": "UNAVAILABLE", "sample_size": min(len(system_errors), len(base)), "validation_status": "INSUFFICIENT DATA"})\n',
